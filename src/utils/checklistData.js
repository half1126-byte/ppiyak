// ì„ íƒëœ ì²´í¬ë¦¬ìŠ¤íŠ¸ í•­ëª©ì„ í…ìŠ¤íŠ¸ë¡œ ë³€í™˜í•˜ëŠ” í—¬í¼ í•¨ìˆ˜
export function convertChecklistToText(selections, additionalText = '') {
    const selectedItems = [];

    Object.entries(selections).forEach(([category, items]) => {
        if (items && items.length > 0) {
            const categoryData = CHECKLIST_DATA[category];
            items.forEach(itemId => {
                const item = categoryData.items.find(i => i.id === itemId);
                if (item) {
                    selectedItems.push(item.label);
                }
            });
        }
    });

    let result = selectedItems.join(', ');
    if (additionalText) {
        result += `. ${additionalText}`;
    }

    return result;
}

